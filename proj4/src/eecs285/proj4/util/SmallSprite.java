package eecs285.proj4.util;

public class SmallSprite {
	public float left;
	public float right;
	public float top;
	public float bottom;
	
	public SmallSprite(float leftEdge, float rightEdge, float topEdge, float bottomEdge){
		left = leftEdge;
		right = rightEdge;
		top = topEdge;
		bottom = bottomEdge;
	}
}